st.info("üí° **–ü—ñ–¥–∫–∞–∑–∫–∞:** –ö–æ–Ω—Å—Ç–∞–Ω—Ç–∏ A, B, C –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—é—Ç—å—Å—è –¥–ª—è —Ä–æ–∑—Ä–∞—Ö—É–Ω–∫—É —Ç–∏—Å–∫—É –Ω–∞—Å–∏—á–µ–Ω–æ—ó –ø–∞—Ä–∏ $P_s$ –∑–∞ —Ñ–æ—Ä–º—É–ª–æ—é: $\lg(P_s) = A - \\frac{B}{C + t}$")

# –°—Ç–≤–æ—Ä—é—î–º–æ –±—ñ—á–Ω—É –ø–∞–Ω–µ–ª—å
with st.sidebar:
    st.header("–í—Ö—ñ–¥–Ω—ñ –¥–∞–Ω—ñ")
    m = st.number_input("–ú–∞—Å–∞ (m), –∫–≥", value=10.0, step=1.0)
    z = st.number_input("–ö–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç (z)", value=0.1, step=0.01, format="%.2f")
    Q_gas_val = st.number_input("–¢–µ–ø–ª–æ—Ç–∞ –∑–≥–æ—Ä—è–Ω–Ω—è —Ä–µ—á–æ–≤–∏–Ω–∏ $Q_{gas}$ (x$10^6$), –î–∂/–∫–≥", value=5.1, step=0.1)
    
    st.markdown("---")
    st.header("üìç –ö–æ–æ—Ä–¥–∏–Ω–∞—Ç–∏ –æ–±'—î–∫—Ç–∞")
    lat = st.number_input("–®–∏—Ä–æ—Ç–∞ (Lat)", value=55.751442, format="%.6f")
    lon = st.number_input("–î–æ–≤–≥–æ—Ç–∞ (Lon)", value=37.615414, format="%.6f")
    
    Q0 = 4.52 * 10**6
    P0 = 101.3

# –†–æ–∑—Ä–∞—Ö—É–Ω–∫–∏
Q_gas = Q_gas_val * 10**6
m_pr = (Q_gas * m * z) / Q0

# –§—É–Ω–∫—Ü—ñ—è –¥–ª—è –∑–Ω–∞—Ö–æ–¥–∂–µ–Ω–Ω—è —Ä–∞–¥—ñ—É—Å–∞ r –¥–ª—è —Ü—ñ–ª—å–æ–≤–æ–≥–æ —Ç–∏—Å–∫—É P_target
def calculate_radius(target_P, m_pr, P0):
    func = lambda r: P0 * (0.8 * m_pr**0.33 / r + 3 * m_pr**0.66 / r**2 + 5 * m_pr / r**3) - target_P
    # –ß–∏—Å–µ–ª—å–Ω–µ –∑–Ω–∞—Ö–æ–¥–∂–µ–Ω–Ω—è –∫–æ—Ä–µ–Ω—è (–ø–æ—á–∞—Ç–∫–æ–≤–µ –Ω–∞–±–ª–∏–∂–µ–Ω–Ω—è 10 –º)
    r_sol = fsolve(func, 10)
    return float(r_sol[0])

# –í–∏–∑–Ω–∞—á–µ–Ω–Ω—è —Ä–∞–¥—ñ—É—Å—ñ–≤ –¥–ª—è –∫—Ä–∏—Ç–∏—á–Ω–∏—Ö –∑–Ω–∞—á–µ–Ω—å
